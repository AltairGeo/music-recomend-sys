from typing import Protocol
from .domains import Track
from typing import Unpack
from .types import ReadTrackFilters

class TracksCrudPort(Protocol):
    async def create(self, model: Track) -> int: ...
    async def get(self, id: int) -> Track: ...
    async def read(self, skip: int = 0, limit: int = 100, **filters: Unpack[ReadTrackFilters]) -> list[Track]: ...
    async def find_by_name(self, name: str) -> list[Track]: ...


class EmbeddingsCrudPort(Protocol):
    async def create(self, vector: list[float]) -> int: ...
    async def delete(self, id: int) -> bool: ...
